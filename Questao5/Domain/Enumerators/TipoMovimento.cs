namespace Questao5.Domain.Enumerators
{
    /// <summary>
    /// Enumeração que representa os tipos de movimento financeiro em uma conta corrente.
    /// </summary>
    public enum TipoMovimento
    {
        /// <summary>
        /// Movimento de crédito, representado pela letra 'C'.
        /// </summary>
        Credito = 'C',

        /// <summary>
        /// Movimento de débito, representado pela letra 'D'.
        /// </summary>
        Debito = 'D'
    }
}
